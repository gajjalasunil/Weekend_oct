package mq.variables;

public class DataTypes 
{

	public static void main(String[] args) {
		
		
		/*
		 * int:--> Numeric data type allow only numbers to store
		 * 			default value is 0
		 */
		int a=100;
		int b=200;
		int c=a+b;
		System.out.println("total value is =>"+c);
		
		
		/*
		 * boolean :--> Accept true/false
		 * 				default value is false
		 */
		boolean flag=true;
		System.out.println("Status is => "+flag);
		
		
		/*
		 * String:-->
		 * 		 => String is a non primitive datatype
		 * 		 => Allow set of characters to store
		 * 		 => default values is null
		 */
		String name="mq-dsnr";
		String mobile="9030248855";
		String address="#310, SreemanRam towers, Chaitanyapur";
		
		
		
		System.out.println(name+"   "+mobile+"   "+address);
		
		
		
		
		

	}

}
